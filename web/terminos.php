(function () {
  var supported = ['es', 'en'];
  var stored = localStorage.getItem('lang');
  var browser = (navigator.language || '').split('-')[0];
  var init = supported.indexOf(stored) !== -1 ? stored
           : supported.indexOf(browser) !== -1 ? browser
           : 'es';

  var footerText = {
    es: 'AIRE es una organización sin fines de lucro registrada en Florida como una Business League, dedicada a la promoción de los intereses comunes de sus miembros y el fortalecimiento del ecosistema empresarial iberoamericano.',
    en: 'AIRE is a nonprofit organization registered in Florida as a Business League, dedicated to promoting the common interests of its members and strengthening the Ibero-American business ecosystem.'
  };

  function setLang(lang) {
    document.querySelectorAll('[data-terminos-content]').forEach(function (el) {
      el.style.display = el.getAttribute('data-terminos-content') === lang ? '' : 'none';
    });
    document.querySelectorAll('.terminos_lang_btn').forEach(function (btn) {
      btn.classList.toggle('terminos_lang_btn_active', btn.getAttribute('data-terminos-lang') === lang);
    });
    var footer = document.getElementById('terminos_footer_legal');
    if (footer && footerText[lang]) footer.textContent = footerText[lang];
    document.documentElement.lang = lang;
    localStorage.setItem('lang', lang);
  }

  document.querySelectorAll('.terminos_lang_btn').forEach(function (btn) {
    btn.addEventListener('click', function () {
      setLang(this.getAttribute('data-terminos-lang'));
    });
  });

  setLang(init);
})();
</script>

